#include<iostream>
using namespace std;
int main()
{
    //compound assignment operators +=,-=,*=,/=,%=
    int a=10;
    cout<<"Compound assignment examples:"<<endl;
    a+=7; //a=a+7
    cout<<a<<endl;
    a-=3;//a=a-3
    cout<<a<<endl;
    a*=3;//a=a*3
    cout<<a<<endl;
    a/=3;//a=a/3
    cout<<a<<endl;
    a%=2;//show remainder:a=a%2
    cout<<a<<endl;
    return 0;

}