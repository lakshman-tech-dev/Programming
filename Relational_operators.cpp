#include<iostream>
using namespace std;
int main()
{
    //Relational operators >,<,<=,>=,==,!=
    int a=10;
    int b=5;
    //used for comparison between two operands
    cout<<(a>b)<<endl;//1
    cout<<(a<b)<<endl;//0
    cout<<(a>=b)<<endl;//greater than or equal to 1
    cout<<(a<=b)<<endl;//0
    cout<<(a==b)<<endl;//0
    cout<<(a!=b)<<endl;//1
    return 0;
}